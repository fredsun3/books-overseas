// Helper functions
export function getBooksByCountry(countryId) {
  if (countryId === 'all') return books;
  return books.filter(b => b.country === countryId);
}

export function getBooksByDynasty(dynastyId) {
  if (dynastyId === 'all') return books;
  return books.filter(b => b.dynasty === dynastyId);
}

export function getBooksByCategory(categoryId) {
  if (categoryId === 'all') return books;
  return books.filter(b => b.category === categoryId);
}

export function searchBooks(query) {
  if (!query) return books;
  const q = query.toLowerCase();
  return books.filter(b =>
    b.title.toLowerCase().includes(q) ||
    b.titleEn.toLowerCase().includes(q) ||
    b.description.toLowerCase().includes(q) ||
    b.dynasty.includes(q) ||
    b.category.includes(q)
  );
}

export function getBookById(id) {
  return books.find(b => b.id === id);
}

export function getInstitutionById(id) {
  return INSTITUTIONS.find(i => i.id === id);
}

export function getCountryName(countryId) {
  const c = COUNTRIES.find(c => c.id === countryId);
  return c ? c.name : countryId;
}

export function getStatistics() {
  const stats = {
    totalBooks: books.length,
    totalCountries: [...new Set(books.map(b => b.country))].length,
    totalInstitutions: [...new Set(books.map(b => b.institution))].length,
    byCountry: {},
    byDynasty: {},
    byCategory: {},
  };

  books.forEach(b => {
    stats.byCountry[b.country] = (stats.byCountry[b.country] || 0) + 1;
    stats.byDynasty[b.dynasty] = (stats.byDynasty[b.dynasty] || 0) + 1;
    stats.byCategory[b.category] = (stats.byCategory[b.category] || 0) + 1;
  });

  return stats;
}
